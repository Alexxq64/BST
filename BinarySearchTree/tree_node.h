#ifndef TREE_NODE_H
#define TREE_NODE_H

/**
 * @file tree_node.h
 * @brief Определение структуры узла бинарного дерева
 *
 * Этот файл содержит базовую структуру TreeNode, которая представляет узел
 * в бинарном дереве поиска. Каждый узел содержит ключ (значение) и указатели
 * на левое и правое поддеревья.
 */

 // Структура узла бинарного дерева
struct TreeNode {
    int key;           // Ключ (значение) узла
    TreeNode* left;    // Указатель на левое поддерево
    TreeNode* right;   // Указатель на правое поддерево

    /**
     * @brief Конструктор для создания нового узла
     * @param k Значение ключа для нового узла
     *
     * Создает узел с заданным ключом и нулевыми указателями на потомков.
     * Это обеспечивает, что новый узел всегда является листом при создании.
     */
    TreeNode(int k) : key(k), left(nullptr), right(nullptr) {}
};

#endif // TREE_NODE_H
